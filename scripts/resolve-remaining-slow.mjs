function loadGeneratedMedia() {
  const fp = join(root, "src", "data", "festival-media.generated.ts");
  const text = fs.readFileSync(fp, "utf8");
  const start = text.indexOf("{", text.indexOf("FESTIVAL_MEDIA_GENERATED"));
  const end = text.lastIndexOf("};");
  return { text, media: JSON.parse(text.slice(start, end + 1)) };
}

function writeGeneratedMedia(media) {
  const ts = `import type { FestivalGalleryImage } from "@/types/festival";

export type FestivalMediaBundle = {
  imageUrl: string;
  imageGallery: FestivalGalleryImage[];
};

/** Auto-generated by scripts/build-festival-media.mjs — do not edit by hand. */
export const FESTIVAL_MEDIA_GENERATED: Record<string, FestivalMediaBundle> = ${JSON.stringify(
    media,
    null,
    2
  )};
`;
  fs.writeFileSync(join(root, "src", "data", "festival-media.generated.ts"), ts, "utf8");
}

function parseDataFile(relPath) {
  const text = fs.readFileSync(join(root, relPath), "utf8");
  const out = [];
  const re = /\{\s*\n\s*id:\s*"([^"]+)"[\s\S]*?\n\s*\}(?=\s*,|\s*\n)/g;
  let m;
  while ((m = re.exec(text))) {
    const block = m[0];
    if (!out.find((x) => x.id === m[1])) {
      out.push({
        id: m[1],
        name: block.match(/name:\s*"([^"]*)"/)?.[1] ?? m[1]
      });
    }
  }
  return out;
}

async function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function imageInfoForTitle(fileTitle) {
  await sleep(3000);
  const url =
    "https://commons.wikimedia.org/w/api.php?action=query&format=json&origin=*" +
    "&prop=imageinfo&iiurlwidth=1400&iiprop=url|extmetadata" +
    `&titles=${encodeURIComponent(fileTitle)}`;
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  const txt = await res.text();
  if (txt.startsWith("You are making")) return { error: "rate_limit" };
  const j = JSON.parse(txt);
  const page = Object.values(j.query?.pages ?? {})[0];
  if (page?.missing !== undefined) return null;
  const ii = page?.imageinfo?.[0];
  if (!ii?.url) return null;
  const ext = ii.extmetadata ?? {};
  const artist = (ext.Artist?.value || ext.Credit?.value || "")
    .replace(/<[^>]+>/g, "")
    .trim();
  const license = (ext.LicenseShortName?.value || "Wikimedia Commons")
    .replace(/<[^>]+>/g, "")
    .trim();
  const credit = artist
    ? `${artist} · Wikimedia Commons · ${license}`
    : `Wikimedia Commons · ${license}`;
  return { thumb: ii.thumburl || ii.url, full: ii.url, credit, title: fileTitle };
}

async function commonsSearch(query) {
  await sleep(3000);
  const url =
    "https://commons.wikimedia.org/w/api.php?action=query&format=json&origin=*" +
    "&list=search&srnamespace=6&srlimit=10" +
    `&srsearch=${encodeURIComponent(query)}`;
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  const txt = await res.text();
  if (txt.startsWith("You are making")) return [];
  const j = JSON.parse(txt);
  return (j.query?.search ?? []).map((s) => s.title);
}

function bundleFor(name, info) {
  return {
    imageUrl: info.thumb || info.full,
    imageGallery: [
      { url: info.full, alt: `${name} — event image`, credit: info.credit },
      { url: info.thumb || info.full, alt: `${name} — event detail`, credit: info.credit }
    ]
  };
}

async function resolveId(f) {
  const verified = VERIFIED[f.id];
  if (verified) {
    const info = await imageInfoForTitle(verified);
    if (info && !info.error) return { info, source: "verified" };
    if (info?.error) return { error: "rate_limit" };
  }

  for (const q of [`${f.name} festival`, `${f.name}`, f.id.replace(/-/g, " ")]) {
    const titles = await commonsSearch(q);
    for (const t of titles) {
      if (!/\.(jpe?g|png|webp)/i.test(t)) continue;
      const info = await imageInfoForTitle(t);
      if (info?.error) return { error: "rate_limit" };
      if (info) return { info, source: `search:${q}` };
    }
  }
  return null;
}

async function main() {
  const report = JSON.parse(
    fs.readFileSync(join(root, "src/data/festival-media-resolve-report.json"), "utf8")
  );
  const targetIds = report.details.stillStock ?? report.details.failed.map((x) => x.id);
  const byId = new Map(parseDataFile("src/data/festivals.ts").concat(parseDataFile("src/data/festivals-more.ts")).map((f) => [f.id, f]));
  const { media } = loadGeneratedMedia();

  const resolved = [];
  const failed = [];

  for (let i = 0; i < targetIds.length; i++) {
    const id = targetIds[i];
    const f = byId.get(id);
    if (!f) continue;
    process.stdout.write(`\r[${i + 1}/${targetIds.length}] ${id}`.padEnd(55));
    const hit = await resolveId(f);
    if (hit?.error === "rate_limit") {
      console.log("\nRate limited — saving progress and stopping.");
      break;
    }
    if (hit?.info) {
      media[id] = bundleFor(f.name, hit.info);
      resolved.push({ id, title: hit.info.title, source: hit.source });
      const overrides = JSON.parse(fs.readFileSync(join(__dirname, "festival-media-overrides.json"), "utf8"));
      overrides.filesById[id] = hit.info.title;
      fs.writeFileSync(join(__dirname, "festival-media-overrides.json"), JSON.stringify(overrides, null, 2) + "\n");
    } else {
      failed.push(id);
    }
  }

  writeGeneratedMedia(media);
  const stillStock = targetIds.filter((id) => /unsplash/i.test(media[id]?.imageUrl ?? ""));
  fs.writeFileSync(
    join(root, "src/data/festival-media-thematic-fallbacks.json"),
    JSON.stringify(
      {
        generatedAt: new Date().toISOString(),
        note: "Festivals still using thematic Unsplash.",
        count: stillStock.length,
        ids: stillStock.sort()
      },
      null,
      2
    ),
    "utf8"
  );
  console.log(`\nResolved: ${resolved.length}, failed: ${failed.length}, still Unsplash: ${stillStock.length}`);
}

main().catch(console.error);
