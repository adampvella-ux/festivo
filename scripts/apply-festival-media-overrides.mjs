/**
 * Resolves Wikimedia images for festivals on the thematic-fallback list (or all overrides)
 * and patches src/data/festival-media.generated.ts — much faster than a full 209-festival rebuild.
 *
 * Run: node scripts/apply-festival-media-overrides.mjs
 *      node scripts/apply-festival-media-overrides.mjs --all-fallbacks
 */
import fs from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const UA = "FestivoWeb/1.0 (festival media resolver)";

const overridesPath = join(__dirname, "festival-media-overrides.json");
const overrides = JSON.parse(fs.readFileSync(overridesPath, "utf8"));
const FILES_BY_ID = overrides.filesById ?? {};
const WIKI_TITLE_BY_ID = overrides.wikipediaTitleById ?? {};

function parseGeneratedCopyMap() {
  const fp = join(root, "src", "data", "festival-content.generated.ts");
  const text = fs.readFileSync(fp, "utf8");
  const start = text.indexOf("{", text.indexOf("FESTIVAL_COPY_GENERATED"));
  const end = text.lastIndexOf("};");
  if (start < 0 || end < 0) return {};
  return JSON.parse(text.slice(start, end + 1));
}

function parseDataFile(relPath) {
  const text = fs.readFileSync(join(root, relPath), "utf8");
  const out = [];
  const re = /\{\s*\n\s*id:\s*"([^"]+)"[\s\S]*?\n\s*\}(?=\s*,|\s*\n)/g;
  let m;
  while ((m = re.exec(text))) {
    const block = m[0];
    out.push({
      id: m[1],
      name: block.match(/name:\s*"([^"]*)"/)?.[1] ?? m[1],
      city: block.match(/city:\s*"([^"]*)"/)?.[1] ?? "",
      country: block.match(/country:\s*"([^"]*)"/)?.[1] ?? ""
    });
  }
  return out;
}

function loadThematicFallbackIds() {
  const fp = join(root, "src", "data", "festival-media-thematic-fallbacks.json");
  if (!fs.existsSync(fp)) return [];
  return JSON.parse(fs.readFileSync(fp, "utf8")).ids ?? [];
}

function loadGeneratedMedia() {
  const fp = join(root, "src", "data", "festival-media.generated.ts");
  const text = fs.readFileSync(fp, "utf8");
  const start = text.indexOf("{", text.indexOf("FESTIVAL_MEDIA_GENERATED"));
  const end = text.lastIndexOf("};");
  return JSON.parse(text.slice(start, end + 1));
}

function writeGeneratedMedia(media) {
  const ts = `import type { FestivalGalleryImage } from "@/types/festival";

export type FestivalMediaBundle = {
  imageUrl: string;
  imageGallery: FestivalGalleryImage[];
};

/** Auto-generated by scripts/build-festival-media.mjs — do not edit by hand. */
export const FESTIVAL_MEDIA_GENERATED: Record<string, FestivalMediaBundle> = ${JSON.stringify(
    media,
    null,
    2
  )};
`;
  fs.writeFileSync(join(root, "src", "data", "festival-media.generated.ts"), ts, "utf8");
}

function metaValue(m, key) {
  const v = m?.[key]?.value;
  if (!v) return "";
  return String(v).replace(/<[^>]+>/g, "").replace(/\s+/g, " ").trim();
}

async function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

async function imageInfoForTitle(fileTitle) {
  const url =
    "https://commons.wikimedia.org/w/api.php?action=query&format=json&origin=*" +
    "&prop=imageinfo&iiurlwidth=1400&iiprop=url|extmetadata" +
    `&titles=${encodeURIComponent(fileTitle)}`;
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  if (!res.ok) return null;
  const j = await res.json();
  const page = Object.values(j.query?.pages ?? {})[0];
  const ii = page?.imageinfo?.[0];
  if (!ii?.url) return null;
  const ext = ii.extmetadata ?? {};
  const artist = metaValue(ext, "Artist") || metaValue(ext, "Credit");
  const license =
    metaValue(ext, "LicenseShortName") || metaValue(ext, "LicensedShortName") || "Wikimedia Commons";
  const shortLicense = license.length > 40 ? "Wikimedia Commons" : license;
  const credit = artist
    ? `${artist} · Wikimedia Commons · ${shortLicense}`
    : `Wikimedia Commons · ${shortLicense}`;
  const mime = metaValue(ext, "MIME_type");
  if (mime.includes("svg") || mime.includes("webm")) return null;
  return { thumb: ii.thumburl || ii.url, full: ii.url, credit, title: fileTitle };
}

async function wikipediaPageImage(articleTitle) {
  const url =
    "https://en.wikipedia.org/w/api.php?action=query&format=json&origin=*" +
    "&prop=pageimages&piprop=thumbnail|original&pithumbsize=1600" +
    `&titles=${encodeURIComponent(articleTitle)}&redirects=1`;
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  if (!res.ok) return null;
  const j = await res.json();
  const page = Object.values(j.query?.pages ?? {})[0];
  const src = page?.original?.source || page?.thumbnail?.source;
  if (!src || !/\.(jpe?g|png|webp)(\?|$)/i.test(src)) return null;
  return {
    thumb: page?.thumbnail?.source || src,
    full: src,
    credit: `Wikimedia (from Wikipedia article "${articleTitle}")`,
    title: articleTitle
  };
}

async function commonsFileSearch(query) {
  const url =
    "https://commons.wikimedia.org/w/api.php?action=query&format=json&origin=*" +
    "&list=search&srnamespace=6&srlimit=8" +
    `&srsearch=${encodeURIComponent(query)}`;
  const res = await fetch(url, { headers: { "User-Agent": UA } });
  if (!res.ok) return [];
  const j = await res.json();
  return (j.query?.search ?? []).map((s) => s.title);
}

function isStockUrl(url) {
  return /images\.unsplash\.com/i.test(url ?? "");
}

function bundleFor(f, info) {
  return {
    imageUrl: info.thumb || info.full,
    imageGallery: [
      { url: info.full, alt: `${f.name} — event image`, credit: info.credit },
      {
        url: info.thumb || info.full,
        alt: `${f.name} — event detail`,
        credit: info.credit
      }
    ]
  };
}

async function resolveFestival(f, copyMap) {
  const explicit = FILES_BY_ID[f.id];
  if (explicit) {
    const info = await imageInfoForTitle(explicit);
    if (info) return { info, source: "override" };
    // Bad override title — fall through to Wikipedia / Commons search.
  }

  const wikiTitle =
    WIKI_TITLE_BY_ID[f.id] ?? copyMap[f.id]?.wikipediaTitle ?? null;
  if (wikiTitle) {
    const info = await wikipediaPageImage(wikiTitle);
    if (info) return { info, source: "wikipedia" };
  }

  for (const q of [
    `${f.name} ${f.city}`,
    `${f.name} festival ${f.country}`,
    `${f.name} ${f.country}`,
    f.name
  ]) {
    const titles = await commonsFileSearch(q);
    await sleep(400);
    for (const t of titles) {
      if (!/\.(jpg|jpeg|png|webp)/i.test(t)) continue;
      const info = await imageInfoForTitle(t);
      await sleep(300);
      if (info) return { info, source: `commons:${q}` };
    }
  }
  return null;
}

async function main() {
  const allFallbacks = process.argv.includes("--all-fallbacks");
  const copyMap = parseGeneratedCopyMap();
  const byId = new Map();
  for (const f of [...parseDataFile("src/data/festivals.ts"), ...parseDataFile("src/data/festivals-more.ts")]) {
    if (!byId.has(f.id)) byId.set(f.id, f);
  }

  const media = loadGeneratedMedia();
  const targetIds = allFallbacks
    ? loadThematicFallbackIds()
    : [...new Set([...Object.keys(FILES_BY_ID), ...loadThematicFallbackIds()])];

  const resolved = [];
  const failed = [];
  const skipped = [];

  for (let i = 0; i < targetIds.length; i++) {
    const id = targetIds[i];
    const f = byId.get(id);
    if (!f) {
      failed.push({ id, reason: "no festival record" });
      continue;
    }
    process.stdout.write(`\r[${i + 1}/${targetIds.length}] ${id}`.padEnd(60));

    const current = media[id];
    if (current && !isStockUrl(current.imageUrl) && !FILES_BY_ID[id] && !allFallbacks) {
      skipped.push(id);
      continue;
    }

    try {
      const hit = await resolveFestival(f, copyMap);
      await sleep(1200);
      if (hit) {
        media[id] = bundleFor(f, hit.info);
        resolved.push({ id, source: hit.source, title: hit.info.title });
      } else {
        failed.push({ id, reason: "no Wikimedia match" });
      }
    } catch (e) {
      failed.push({ id, reason: String(e.message || e) });
    }
  }

  console.log("\nWriting patched media registry…");
  writeGeneratedMedia(media);

  const stillStock = targetIds.filter((id) => isStockUrl(media[id]?.imageUrl));
  const reportPath = join(root, "src", "data", "festival-media-resolve-report.json");
  fs.writeFileSync(
    reportPath,
    JSON.stringify(
      {
        generatedAt: new Date().toISOString(),
        resolved: resolved.length,
        failed: failed.length,
        skipped: skipped.length,
        stillUsingUnsplash: stillStock.length,
        details: { resolved, failed, stillStock }
      },
      null,
      2
    ),
    "utf8"
  );

  fs.writeFileSync(
    join(root, "src", "data", "festival-media-thematic-fallbacks.json"),
    JSON.stringify(
      {
        generatedAt: new Date().toISOString(),
        note: "Festivals still using thematic Unsplash because no Wikimedia match was found.",
        count: stillStock.length,
        ids: stillStock.sort()
      },
      null,
      2
    ),
    "utf8"
  );

  console.log(`Resolved: ${resolved.length}, failed: ${failed.length}, skipped: ${skipped.length}`);
  console.log(`Still on Unsplash: ${stillStock.length}`);
  console.log("Report:", reportPath);
  if (failed.length) console.log("Failed ids:", failed.map((x) => x.id).join(", "));
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
